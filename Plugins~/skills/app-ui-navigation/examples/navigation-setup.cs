/// <summary>
/// Custom navigation visual controller that manages AppBar, Drawer, BottomNavBar, and NavigationRail.
/// This example demonstrates a bottom navigation pattern suitable for mobile-like UIs.
/// </summary>
public class AppNavVisualController : INavVisualController
{
    public void SetupAppBar(AppBar appBar, NavDestination destination, NavController navController)
    {
        // The AppBar title is commonly set based on the destination
        appBar.title = destination.label;
        appBar.showMenuButton = false; // No drawer in this example
    }

    public void SetupDrawer(Drawer drawer, NavDestination destination, NavController navController)
    {
        // In this example, we use BottomNavBar instead of Drawer
        // But if you need a drawer, implement it here
        drawer.Add(new DrawerHeader { title = "Navigation" });
        drawer.Add(new Divider { vertical = false });

        var homeItem = new MenuItem { icon = "home", label = "Home", selectable = true };
        homeItem.SetValueWithoutNotify(destination.name == Destinations.Home);
        homeItem.clickable.clicked += () => navController.Navigate(Actions.NavigateToHome);
        drawer.Add(homeItem);
    }

    public void SetupBottomNavBar(BottomNavBar bottomNavBar, NavDestination destination, NavController navController)
    {
        // Home navigation item
        var homeItem = new BottomNavBarItem("home", "Home", () => navController.Navigate(Actions.NavigateToHome))
        {
            isSelected = destination.name == Destinations.Home
        };
        bottomNavBar.Add(homeItem);

        // Explore navigation item
        var exploreItem = new BottomNavBarItem("explore", "Explore", () => navController.Navigate(Actions.NavigateToExplore))
        {
            isSelected = destination.name == Destinations.Explore
        };
        bottomNavBar.Add(exploreItem);

        // Saved navigation item
        var savedItem = new BottomNavBarItem("bookmark", "Saved", () => navController.Navigate(Actions.NavigateToSaved))
        {
            isSelected = destination.name == Destinations.Saved
        };
        bottomNavBar.Add(savedItem);

        // Profile navigation item
        var profileItem = new BottomNavBarItem("person", "Profile", () => navController.Navigate(Actions.NavigateToProfile))
        {
            isSelected = destination.name == Destinations.Profile
        };
        bottomNavBar.Add(profileItem);
    }

    public void SetupNavigationRail(NavigationRail navigationRail, NavDestination destination, NavController navController)
    {
        navigationRail.anchor = NavigationRailAnchor.Start;
        navigationRail.labelType = LabelType.Selected;

        var homeItem = new NavigationRailItem { icon = "home", label = "Home", selected = destination.name == Destinations.Home };
        homeItem.clickable.clicked += () => navController.Navigate(Actions.NavigateToHome);
        navigationRail.mainContainer.Add(homeItem);

        var exploreItem = new NavigationRailItem { icon = "explore", label = "Explore", selected = destination.name == Destinations.Explore };
        exploreItem.clickable.clicked += () => navController.Navigate(Actions.NavigateToExplore);
        navigationRail.mainContainer.Add(exploreItem);
    }
}

