/// <summary>
/// Example MonoBehaviour that sets up the navigation system.
/// This demonstrates how to initialize NavHost with a navigation graph
/// and connect it to your UI.
/// </summary>
public class NavigationAppSetup : MonoBehaviour
{
    private NavHost m_NavHost;
    private NavGraphViewAsset m_NavigationGraph;

    private void OnEnable()
    {
        SetupNavigation();
    }

    private void SetupNavigation()
    {
        // Get the root UI element (usually from UIDocument)
        var rootElement = GetComponent<UIDocument>()?.rootVisualElement;
        if (rootElement == null)
        {
            Debug.LogError("UIDocument not found");
            return;
        }

        // Create or load the navigation graph
        // In production, you would typically load this as an asset:
        // m_NavigationGraph = Resources.Load<NavGraphViewAsset>("Navigation/MainGraph");
        m_NavigationGraph = NavigationGraphFactory.CreateNavigationGraph();

        // Create the NavHost
        m_NavHost = new NavHost();

        // Set the visual controller
        m_NavHost.visualController = new AppNavVisualController();

        // Load the navigation graph
        m_NavHost.LoadGraph(m_NavigationGraph);

        // Subscribe to navigation events
        m_NavHost.onNavigate += OnNavigate;
        m_NavHost.onDestinationEnter += OnDestinationEnter;
        m_NavHost.onDestinationExit += OnDestinationExit;

        // Add to UI
        rootElement.Add(m_NavHost);
    }

    private void OnNavigate(NavController controller, NavDestination destination)
    {
        Debug.Log($"Navigated to: {destination.label}");
    }

    private void OnDestinationEnter(NavController controller, NavDestination destination)
    {
        Debug.Log($"Entered destination: {destination.label}");
    }

    private void OnDestinationExit(NavController controller, NavDestination destination)
    {
        Debug.Log($"Exited destination: {destination.label}");
    }
}

