/// <summary>
/// Details screen that receives arguments from navigation.
/// Demonstrates how to extract and use arguments from the navigation call.
/// </summary>
public class DetailsScreen : NavigationScreen
{
    private Label m_TitleLabel;
    private Label m_IdLabel;

    public override void OnEnter(NavController controller, NavDestination destination, Argument[] args)
    {
        base.OnEnter(controller, destination, args);

        // Extract arguments from navigation
        string itemId = "unknown";
        string itemName = "Unknown Item";

        foreach (var arg in args)
        {
            if (arg.key == "itemId")
                itemId = arg.value.ToString();
            else if (arg.key == "itemName")
                itemName = arg.value.ToString();
        }

        // Display the details
        m_TitleLabel = new Label($"Item: {itemName}");
        m_TitleLabel.style.fontSize = 20;
        m_TitleLabel.style.marginTop = 20;
        Add(m_TitleLabel);

        m_IdLabel = new Label($"ID: {itemId}");
        m_IdLabel.style.marginTop = 10;
        Add(m_IdLabel);

        // Back button
        var backButton = new Button(() =>
        {
            if (controller.canNavigateBack)
                controller.NavigateBack();
        })
        {
            text = "Go Back"
        };
        backButton.style.marginTop = 20;
        Add(backButton);
    }

    protected override void SetupAppBar(AppBar appBar, NavController navController)
    {
        base.SetupAppBar(appBar, navController);
        appBar.title = "Details";
        // AppBar will show back button automatically due to back stack
    }
}

