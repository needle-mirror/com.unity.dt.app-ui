/// <summary>
/// Factory for creating the navigation graph at runtime.
/// This demonstrates programmatic graph construction.
/// In practice, you would typically create graphs in the Navigation Graph Editor
/// and load them as assets.
/// </summary>
public static class NavigationGraphFactory
{
    public static NavGraphViewAsset CreateNavigationGraph()
    {
        // Create the main graph
        var mainGraph = new NavGraph { name = "main" };

        // Create destinations
        var homeDestination = CreateDestination(
            Destinations.Home,
            "Home",
            typeof(HomeScreen),
            showAppBar: true,
            showBottomNavBar: true
        );

        var exploreDestination = CreateDestination(
            Destinations.Explore,
            "Explore",
            typeof(ExploreScreen),
            showAppBar: true,
            showBottomNavBar: true
        );

        var savedDestination = CreateDestination(
            Destinations.Saved,
            "Saved",
            typeof(SavedScreen),
            showAppBar: true,
            showBottomNavBar: true
        );

        var profileDestination = CreateDestination(
            Destinations.Profile,
            "Profile",
            typeof(ProfileScreen),
            showAppBar: true,
            showBottomNavBar: true
        );

        var detailsDestination = CreateDestination(
            Destinations.Details,
            "Details",
            typeof(DetailsScreen),
            showAppBar: true,
            showBottomNavBar: false
        );

        var loginDestination = CreateDestination(
            Destinations.Login,
            "Login",
            typeof(LoginScreen),
            showAppBar: true,
            showBottomNavBar: false
        );

        // Add destinations to graph
        mainGraph.destinations.Add(homeDestination);
        mainGraph.destinations.Add(exploreDestination);
        mainGraph.destinations.Add(savedDestination);
        mainGraph.destinations.Add(profileDestination);
        mainGraph.destinations.Add(detailsDestination);
        mainGraph.destinations.Add(loginDestination);

        // Set home as start destination
        mainGraph.startDestination = homeDestination;

        // Create navigation actions
        var navigateToHomeAction = new NavAction
        {
            actionId = Actions.NavigateToHome,
            destination = homeDestination,
            backStackBehavior = BackStackBehavior.PopUntilInclusive
        };
        mainGraph.globalActions.Add(navigateToHomeAction);

        var navigateToExploreAction = new NavAction
        {
            actionId = Actions.NavigateToExplore,
            destination = exploreDestination,
            backStackBehavior = BackStackBehavior.PopUntilInclusive
        };
        mainGraph.globalActions.Add(navigateToExploreAction);

        var navigateToSavedAction = new NavAction
        {
            actionId = Actions.NavigateToSaved,
            destination = savedDestination,
            backStackBehavior = BackStackBehavior.PopUntilInclusive
        };
        mainGraph.globalActions.Add(navigateToSavedAction);

        var navigateToProfileAction = new NavAction
        {
            actionId = Actions.NavigateToProfile,
            destination = profileDestination,
            backStackBehavior = BackStackBehavior.PopUntilInclusive
        };
        mainGraph.globalActions.Add(navigateToProfileAction);

        var viewDetailsAction = new NavAction
        {
            actionId = Actions.ViewDetails,
            destination = detailsDestination,
            backStackBehavior = BackStackBehavior.Push
        };
        mainGraph.globalActions.Add(viewDetailsAction);

        var logoutAction = new NavAction
        {
            actionId = Actions.Logout,
            destination = loginDestination,
            backStackBehavior = BackStackBehavior.ClearStack
        };
        mainGraph.globalActions.Add(logoutAction);

        // Create the asset
        var graphViewAsset = ScriptableObject.CreateInstance<NavGraphViewAsset>();
        graphViewAsset.graphs.Add(mainGraph);

        return graphViewAsset;
    }

    private static NavDestination CreateDestination(
        string name,
        string label,
        Type screenType,
        bool showAppBar = true,
        bool showBottomNavBar = false
    )
    {
        return new NavDestination
        {
            name = name,
            label = label,
            template = new DefaultDestinationTemplate
            {
                screenType = screenType,
                showAppBar = showAppBar,
                showBottomNavBar = showBottomNavBar,
                showDrawer = false,
                showNavigationRail = false
            }
        };
    }
}

