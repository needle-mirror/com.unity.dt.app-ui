/// <summary>
/// Base navigation screen with standard setup.
/// This demonstrates how to override SetupAppBar and SetupBottomNavBar
/// for per-screen customization in addition to the global visual controller.
/// </summary>
public class HomeScreen : NavigationScreen
{
    private Label m_ContentLabel;

    public override void OnEnter(NavController controller, NavDestination destination, Argument[] args)
    {
        base.OnEnter(controller, destination, args);

        // Create content for this screen
        m_ContentLabel = new Label("Welcome to Home Screen");
        m_ContentLabel.style.fontSize = 24;
        m_ContentLabel.style.marginTop = 20;
        m_ContentLabel.style.marginBottom = 20;
        Add(m_ContentLabel);

        // Add a button to navigate to details
        var detailsButton = new Button(() => controller.Navigate(Actions.ViewDetails, new[]
        {
            new Argument { key = "itemId", value = "home-item-1" },
            new Argument { key = "itemName", value = "Home Item" }
        }))
        {
            text = "View Details"
        };
        detailsButton.style.marginTop = 10;
        Add(detailsButton);
    }

    protected override void SetupAppBar(AppBar appBar, NavController navController)
    {
        base.SetupAppBar(appBar, navController);
        appBar.title = "Home";

        // Add custom action button for home screen
        var refreshButton = new ActionButton { icon = "refresh", label = "Refresh" };
        refreshButton.clickable.clicked += () => Debug.Log("Refresh clicked");
        appBar.Add(refreshButton);
    }

    public override void OnExit(NavController controller, NavDestination destination, Argument[] args)
    {
        base.OnExit(controller, destination, args);
        // Cleanup code if needed
    }
}

